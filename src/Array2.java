public class Array2 {
    public static void main(String[] args) {
        int[][] Array2 = new int[][]{{1,2,3},{4,5,6}};
        for(int i = 0; i<Array2.length; i++){
            for(int j = 0; j<Array2[i].length; j++){
                System.out.print(Array2[i][j] + " ");
            }
            System.out.println();
        }
        System.out.println();
        
        

    }
}
